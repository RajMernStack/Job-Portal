export const getJobStats = async (req, res) => {
  try {
    // Run all 3 counts in parallel for speed
    const [jobs, companies, users] = await Promise.all([
      Job.countDocuments(), // total jobs
      Company.countDocuments(), // total companies
      User.countDocuments({ role: 'student' }) // only job seekers, not recruiters
    ]);

    return res.status(200).json({
      success: true,
      jobs,
      companies,
      users
    });
  } catch (error) {
    console.log(error);
    return res.status(500).json({
      success: false,
      message: "Failed to fetch stats"
    });
  }
};